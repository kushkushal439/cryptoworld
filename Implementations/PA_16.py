def elgamal_keygen(group_size="large"):
    """
    Key Generation for ElGamal.
    Returns: (sk, pk) where sk=x, pk=(p, g, q, h)
    """
    p, g, q = get_group(group_size)
    # 1. Sample private key x from Zq
    x = random.randint(1, q - 1)
    
    # 2. Compute public key h = g^x mod p
    h = pow(g, x, p)
    
    sk = x
    pk = (p, g, q, h)
    return sk, pk

def Enc(pk: tuple, m: int) -> tuple[int, int]:
    """
    Encryption of m in G.
    Returns: (c1, c2) = (g^r mod p, m * h^r mod p)
    """
    p, g, q, h = pk
    
    # 1. Sample fresh r in Zq
    r = random.randint(1, q - 1)
    
    # 2. Output C = (c1, c2)
    c1 = pow(g, r, p)
    c2 = (m * pow(h, r, p)) % p
    
    return c1, c2

def Dec(sk: int, pk: tuple, c1: int, c2: int) -> int:
    """
    Decryption of (c1, c2).
    Returns: m = c2 / c1^x mod p
    """
    p, g, q, h = pk
    x = sk
    
    # Compute the inverse of c1^x mod p
    s = pow(c1, x, p)
    s_inv = pow(s, -1, p)
    
    # m = c2 * s^{-1} mod p
    m = (c2 * s_inv) % p
    return m

